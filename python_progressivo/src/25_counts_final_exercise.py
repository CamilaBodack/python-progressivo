val_1 = float(input("Insira um valor:"))
val_2 = float(input("Insira outro valor:"))

add = val_1 + val_2
subt = val_1 - val_2
mult = val_1 * val_2
division = val_1/val_2
exp = val_1 ** val_2
mod = val_1 % val_2



print("Soma:", val_1, "+", val_2, "=", add)
print("Subtração:", val_1, "-", val_2, "=", subt)
print("Multiplicação:", val_1, "x", val_2, "=", mult)
print("Divisão:", val_1, "/ ", val_2, "=", division)
print("Exponenciação:", val_1, "**", val_2, "=", exp)
print("Resto da divisão:", val_1, "%", val_2, "=", mod)
