num_1 = int(input("Primeiro"))
num_2 = int(input("Segundo"))
num_3 = int(input("Terceiro"))

if num_1 > num_2:
    print("Primeiro é o maior")
elif num_2 > num_3:
    print("Segundo é o maior")
else:
    print("O terceiro é o maior")

if num_1 < num_2:
    print("O primeiro é o menor")
elif num_2 < num_3:
    print("O segundo é o menor")
else:
    print("O terceiro é o menor")
