day = int(input("Dia da semana"))

if day == 1:
    print("1 - Segunda")
    
elif day == 2:
    print("2 - Terça")

elif day == 3:
    print("3 - Quarta")

elif day == 4:
    print("4 - Quinta")

elif day == 5:
    print("5 - Sexta")

elif day == 6:
    print("6 - Sábado")

elif day == 7:
    print("7 - Domingo")

else:
    print("Inválido")
